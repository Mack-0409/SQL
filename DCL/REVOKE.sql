DCL [Data Control Language]
This statement is used to control the flow of data.

There are 2 types of statements
1. GRANT
2. REVOKE

2. REVOKE 
      It is used to take back the given permission from user.

SYNTAX - REVOKE SQL_STATEMENT ON TABLE_NAME
         FROM USER_NAME;   

EXAMPLE - REVOKE SELECT ON EMP
          FROM HR;  